class User {
    constructor(userID, name, email, password, age, phone, isSuper, isAdmin) {
      this.userID = userID;
      this.name = name;
      this.email = email;
      this.password = password;
      this.age = age;
      this.phone = phone;
      this.isSuper = isSuper;
      this.isAdmin = isAdmin;
    }
  }