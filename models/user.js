class User {
    constructor(userID, name, email, password, birthdate, phone, isSuper, isAdmin) {
      this.userID = userID;
      this.name = name;
      this.email = email;
      this.password = password;
      this.birthdate = birthdate;
      this.phone = phone;
      this.isSuper = isSuper;
      this.isAdmin = isAdmin;
    }
  }